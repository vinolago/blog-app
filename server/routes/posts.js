// Helper function to resolve category name/ID to ObjectId
const resolveCategory = async (categoryValue) => {
  if (!categoryValue || (typeof categoryValue === 'string' && !categoryValue.trim())) {
    throw new Error('Category is required');
  }

  try {
    // Check if it's already a valid MongoDB ObjectId
    const mongoose = require('mongoose');
    if (mongoose.Types.ObjectId.isValid(categoryValue)) {
      // It's an ObjectId - check if category exists
      const existingById = await Category.findById(categoryValue);
      if (existingById) {
        return categoryValue;
      }
    }
    
    // Try to find existing category by name
    let category = await Category.findOne({ name: categoryValue });
    
    if (!category) {
      // Create new category if it doesn't exist
      // The slug will be auto-generated by the pre-save hook
      category = await Category.create({ 
        name: categoryValue,
        description: `Posts related to ${categoryValue}`
      });
      console.log(`Created new category: ${categoryValue} with slug: ${category.slug}`);
    }
    
    return category._id;
  } catch (error) {
    console.error('Error resolving category:', error);
    throw new Error(`Failed to resolve category: ${error.message}`);
  }
};

// GET /api/posts - Get/list all posts
router.get('/', async (req, res) => {
    try {
        const {
            category,
            author,
            tag,
            sort = '-createdAt',
            page = 1,
            limit = 10,
            select = 'title slug excerpt featuredImage category author viewCount createdAt updatedAt',
        } = req.query;

        const query = {};
        if (category) query.category = category;
        if (author) query.author = author;
        if (tag) query.tags = { $in: [tag] };

        const pageNumber = parseInt(page, 10);
        const pageSize = parseInt(limit, 10);
        const skip = (pageNumber - 1) * pageSize;

        const posts = await Post.find(query)
            .select(select)
            .populate('category', 'name slug')
            .populate('author', 'name email')
            .sort(sort)
            .skip(skip)
            .limit(pageSize);
        
        const totalPosts = await Post.countDocuments(query);


        res.status(200).json({
            success: true,
            count: posts.length,
            total: totalPosts,
            page: pageNumber,
            data: posts,
            totalPages: Math.ceil(totalPosts / pageSize),
        });
    } catch (error) {
        console.error(error);
        res.status(500).json({
            success: false,
            error: error.message || 'Server Error'
        });
    }
});

// GET /api/posts/slug/:slug - Get a specific blog post by slug (for external apps)
