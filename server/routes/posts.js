// POST /api/posts/ - Create a new blog post
router.post('/', fakeAuth, async (req, res) => {
    try {
        const { title, content, author, category, featuredImage, excerpt, tags, isPublished } = req.body;

        console.log('Received category:', category, 'type:', typeof category);

        // Use authenticated user ID if available, otherwise use provided author
        const authorId = req.user ? req.user._id : author;

        // Resolve category name to ObjectId only if category is provided
        let categoryId = null;
        if (category && category.trim()) {
          categoryId = await resolveCategory(category);
        } else if (isPublished) {
          // If publishing, category is required
          throw new Error('Category is required');
        }

        const newPost = new Post({
            title,
            content,
            author: authorId,
            category: categoryId,
            featuredImage,
            excerpt,
            tags,
            isPublished,
        });

        const savedPost = await newPost.save();
        res.status(201).json({
            success: true,
            data: savedPost,
        });
    } catch (error) {
        console.error('Post creation error:', error);
        res.status(500).json({
            success: false,
            error: error.message || 'Server Error'
        });
    }
});

// PUT /api/posts/:idOrSlug - Update an existing blog post by ID or slug
router.put('/:idOrSlug', fakeAuth, async (req, res) => {
    try {
        const { idOrSlug } = req.params;
        const updateData = { ...req.body };

        // Use authenticated user ID if available
        if (req.user) {
            updateData.author = req.user._id;
        }

        // Resolve category name to ObjectId if category is being updated
        if (updateData.category === null || updateData.category === "") {
          // Allow clearing category if explicitly set to null
          delete updateData.category;
        } else if (updateData.category && updateData.category.trim()) {
          // Only resolve if it's a non-empty string
          const resolvedCat = await resolveCategory(updateData.category);
          updateData.category = resolvedCat;
        } else if (updateData.isPublished) {
          // If publishing without category, throw error
          const currentPost = await Post.findOne({ slug: idOrSlug });
          if (!currentPost?.category) {
            throw new Error('Category is required');
          }
          delete updateData.category;
        }

        // Find by slug first, then by ID
        let updatedPost = await Post.findOneAndUpdate({ slug: idOrSlug }, updateData, {
            new: true,
            runValidators: true,
        });

        // If not found by slug, try by ID
        if (!updatedPost) {
            updatedPost = await Post.findByIdAndUpdate(idOrSlug, updateData, {
                new: true,
                runValidators: true,
            });
        }

        if (!updatedPost) {
            return res.status(404).json({
                success: false,
                error: 'Blog post does not exist',
            });
        }

        res.json({
            success: true,
            data: updatedPost,
        });
    } catch (error) {
        console.error('Post update error:', error);
        res.status(500).json({
            success: false,
            error: error.message || 'Server Error',
        });
    }
});

// DELETE /api/posts/:idOrSlug - Delete a blog post by ID or slug (protected: admin only)
router.delete('/:idOrSlug', fakeAuth, async (req, res) => {
    try {
        const { idOrSlug } = req.params;

        // Try to find by slug first, then by ID
        let deletedPost = await Post.findOne({ slug: idOrSlug });
        
        if (!deletedPost) {
            deletedPost = await Post.findById(idOrSlug);
        }

        if (!deletedPost) {
            return res.status(404).json({
                success: false,
                error: 'Blog post does not exist',
            });
        }

        // Delete image from Cloudinary if it's a Cloudinary URL
        if (deletedPost.featuredImage && deletedPost.featuredImage.includes('cloudinary')) {
            try {
                const publicId = deletedPost.featuredImage.split('/').pop().split('.')[0];
                await cloudinary.uploader.destroy(`blog-app/${publicId}`);
            } catch (err) {
                console.error('Error deleting image from Cloudinary:', err);
            }
        }

        await deletedPost.deleteOne();

        res.json({
            success: true,
            message: 'Blog post deleted successfully',
        });
    } catch (error) {
        console.error(error);
        res.status(500).json({
            success: false,
            error: error.message || 'Server Error',
        });
    }
});

